if __name__ == '__main__':
    s = input()
    if s.isalnum()==True:
        print("True")
    else:
        print("False")

        

    if s.isalpha()==True:
        print("True")
    else:
        print("False")


    if s.isdigit()==True:
        print("True")
    else:
        print("False")


    if s.islower()==True:
        print("True")
    else:
        print("False")

    if s.isupper()==True:
        print("True")
    else:
        print("False")
    

